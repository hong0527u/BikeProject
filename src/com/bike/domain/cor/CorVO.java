package com.bike.domain.cor;

public class CorVO {
	private String cor_code;
	private String cor_name;
	private String cor_tel;
	private String cor_adr;
	public String getCor_code() {
		return cor_code;
	}
	public void setCor_code(String cor_code) {
		this.cor_code = cor_code;
	}
	public String getCor_name() {
		return cor_name;
	}
	public void setCor_name(String cor_name) {
		this.cor_name = cor_name;
	}
	public String getCor_tel() {
		return cor_tel;
	}
	public void setCor_tel(String cor_tel) {
		this.cor_tel = cor_tel;
	}
	public String getCor_adr() {
		return cor_adr;
	}
	public void setCor_adr(String cor_adr) {
		this.cor_adr = cor_adr;
	}
	
}
